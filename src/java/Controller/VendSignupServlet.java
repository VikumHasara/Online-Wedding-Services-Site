    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        
        String fname = request.getParameter("fname");
        String lname = request.getParameter("lname");
        String comp = request.getParameter("company");
        String cat = request.getParameter("category");
        String email = request.getParameter("email");
        String pwd = request.getParameter("pword");
        String nic = request.getParameter("nic");
        String gend = request.getParameter("gender");
        String city = request.getParameter("city");
        String mob = request.getParameter("mobile");
        String app = request.getParameter("approval");
         
        
        PrintWriter out = response.getWriter();
        VendSignupDAO rb = new VendSignupDAO();
        
        try {
            rb.input(fname,lname,comp,cat,email,pwd,nic,gend,city,mob,app);
            response.sendRedirect("vend_login.jsp"); 
        }
        catch (Exception e) {
            out.println(e);
        }
    }

